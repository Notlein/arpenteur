      <div class="col-p" style="margin-top:2em" id="mid-col">
        <p><strong><a href="https://www.jeanyvesfrechette.com/" target="_blank" rel="noopener noreferrer">Jean-Yves Fréchette</a></strong> vit à Québec et se joint au milieu de la performance au début des
          années ‘80. Membre du collectif Inter/Le Lieu, il a participé à de grands projets artistiques dont
          Territoires nomades. Fondateur de la Centrale textuelle de Saint-Ubald, il a notamment réalisé <i>Physitexte</i>
          (performance d’édition), <i>Agrotexte</i> (Land Art textuel), <i>Le lieu-dit le lieu</i> (manœuvre textuelle d’écriture
          technique) et <i>Le party textuel</i> (manœuvre-réseau d’écriture collective). Il a cofondé l’Institut de
          twittérature comparée de Québec-Bordeaux et conçu des logiciels éducatifs, notamment LogiTexte, Scriptor
          et Twittexte.</p>
          <p><strong><a href="https://www.facebook.com/bleudiode" target="_blank" rel="noopener noreferrer">Bleu diode</a></strong> est le duo d’artistes multimédia formé par Isabelle Gagné et Sven dans la
          continuité du Mouvement Art Mobile (MAM). Bleu diode conçoit des dispositifs qui intègrent l’art du remix
          comme élément d’intrusion perturbateur, où le facteur humain agit en algorithme imprévu dans un
          environnement toujours plus automatisé.</p>
        <p><strong><a href=http://www.isabellegagne.ca/ target="_blank" rel="noopener noreferrer">Isabelle Gagné</a></strong> est une artiste multimédia qui vit et travaille à Mirabel. Dans sa pratique,
          elle utilise principalement le geste photographique et divers langages numériques. Au Canada comme à
          l’étranger, son travail a fait l’objet d’importantes expositions individuelles et collectives, notamment dans
          le cadre du Mois de la Photo de Montréal (Momenta), aux Rencontres internationales de la photographie en
          Gaspésie, chez Sporobole dans le cadre d’Espace [IM] Média et à la Chaire de recherche du Canada sur les Arts
          et les Littératures Numériques ainsi que chez ADA-X avec Les Courants. Elle a réalisé plusieurs résidences
          d’artistes, rédigé des essais pour des publications artistiques et photographiques, ses œuvres figurent au
          sein de plusieurs collections privées et dans la politique d’intégration des arts à l’architecture du MCCQ.
          Elle est activement impliquée dans le milieu culturel, des arts et du numérique, entre autres comme
          Wikimédienne en résidence pour les régions des Laurentides,Lanaudière et Laval. Pionnière de l’art mobile,
          elle fut également cofondatrice du Mouvement d’art Mobile (MAM).</p>
        <p><strong><a href="https://www.pixetoile.com/" target="_blank" rel="noopener noreferrer">Stefan Buridans, alias Sven</a></strong>, est un artiste multimédia qui vit et travaille à Montréal
          depuis 2007. Diplômé d’études supérieures en cinématographie (ESEC, Paris), il porte un intérêt
          particulier à l’empreinte visuelle laissée sur notre mémoire collective. Ses recherches en art numérique
          sont le fruit d’expériences professionnelles en audiovisuel et invitent le spectateur à traverser l’espace
          indéfini entre la perception et l’interprétation d’une image. Cofondateur et membre actif du Mouvement Art
          Mobile (MAM), Sven est initiateur d’événements dans l’espace public et numérique. Il intervient également
          dans les champs institutionnel et universitaire pour la promotion et la reconnaissance de la notion
          contemporaine de mobilité en art. Son premier solo Plongeons, un dispositif qui intègre la réalité
          augmentée, est actuellement en tournée dans les centres d’art et maisons de la culture du Québec.<p>
            <p><strong><a href=https://www.mouvementartmobile.com/ target="_blank" rel="noopener noreferrer">Mouvement Art Mobile</a></strong>, fondé en 2011, est un collectif d’artistes pluridisciplinaires
              dirigé par Isabelle Gagné et Sven qui ont pour motivation commune d’intégrer les plateformes mobiles
              dans leur geste créatif. Le MAM est un laboratoire où sont lancées des pistes de réflexion qui servent
              de matière première à la réalisation d’évènements, d’interventions et d’œuvres transmédia. Le MAM a
              entre autres déployé le dispositif Si tu connais un lien au Musée de la civilisation de Québec, a
              commissarié Mobilisations, la première rétrospective d’art mobile au Canada, et organise la performance
              sur Twitter Particules - Correspondances inattendues avec Culture pour tous, Littérature québécoise
              mobile du Laboratoire NT2 dans le cadre des Journées de la culture.</p>
