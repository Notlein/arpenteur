        <p><strong>Isabelle Gagné et Sven [Bleu diode]</strong> s’inspirent des travaux de l’artiste multidisciplinaire
          <strong>Jean-Yves Fréchette</strong> depuis 10 ans. Précurseur en art numérique, Fréchette inscrit ses
          recherches dans la pratique d’une écriture performative, à la rigueur mathématique, par l’accumulation de
          données et le défrichage de territoires d’expressions, qu’ils soient technologiques (twittérature) ou non
          (land art). Homme de lettres mais aussi de mesures et contraintes chiffrées, il se définit lui-même
          «&nbsp;géolocalisateur textuel&nbsp;».</p>
        <p>À partir des nombreuses archives récoltées ces dernières années, le duo, dont la démarche repose sur le
          patrimoine et la mémoire, jette les bases de <i>L’ARPENTEUR</i>.</p>
        <p>Cette œuvre hybride numérique, installative et performative explore les travaux majeurs de Fréchette réalisés
          depuis le début des années ‘80. Cette fluidité entre les univers artistiques de Bleu diode et de Jean-Yves
          Fréchette est représentée par les trois espaces occurrents que sont l'Internet, le lieu d’exposition du centre
          de création numérique TOPO à Montréal, ainsi que le quartier général de Fréchette à Saint-Ubalde, où un grand
          nombre de ses créations furent réalisées et documentées.</p>
        <p>L’élément déclencheur de ce dispositif est le projet <em>Agrotexte</em> qui fête ses 40 ans. Jean-Yves
          Fréchette avait fait tracer par des tracteurs le poème aussi gigantesque que minimaliste «&nbsp;TEXTE TERRE
          TISSE&nbsp;» sur les terres agricoles de Saint-Ubalde. Aujourd’hui Fréchette devient L’ARPENTEUR et retourne
          sur les mêmes terres qu’en 1982 pour une performance d’écriture pédestre connectée, guidée d'indices envoyés
          par la poste à la manière de <em>Plis sous pli</em> (1983). Les données géolocalisées de ses marches en
          lettres cursives sont extraites et reproduites dans une œuvre Web inédite diffusée en temps réel dans la
          vitrine de TOPO.</p>
        <p>L’espace d’exposition est une reconstitution théâtrale à petite échelle du milieu de vie de l’artiste et une
          cabine de diffusion multi-écrans. Bleu diode propose au visiteur de jeter un regard à distance sur la bulle
          intime de Fréchette ainsi que sur ce bout de territoire transformé par le développement urbain et la crise
          agricole. La vitrine est aussi un second espace performatif à Montréal où le duo intervient pour y déplacer,
          ajouter ou supprimer des objets, suggérant le passage de L’ARPENTEUR, pourtant dans la région de Portneuf.</p>
        <p>Fréchette interroge le statut de l’art sociologique des années '80 et réenclenche une dynamique de production
          textuelle qui s'inscrit sur une surface de plus de 1,6 km. Le texte est aujourd’hui dématérialisé et
          appartient à la logique de transmission du Web et des GPS. Fréchette est présent dans le champ du texte à
          Saint-Ubalde pour y produire quotidiennement d’immenses textes par la marche. Ruralité, textualité,
          performance et land art, la démarche de Fréchette est à la fois festive et numérique : elle témoigne du
          plaisir d'écrire et de tracer des signes sur une surface.</p>
        <p>Avec <i>L’ARPENTEUR</i>, Bleu diode revisite plus largement les travaux phares de la Centrale textuelle de
          Saint-Ubalde pour restituer l’importance de l'œuvre de Jean-Yves Fréchette dans les pratiques non seulement
          littéraires, mais aussi performatives et numériques contemporaines.</p>
